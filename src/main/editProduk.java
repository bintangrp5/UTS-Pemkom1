    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        jTextField1.setText(getKode());
        jTextField2.setText(getNama());
        jTextField4.setText(getGambar());
        for (int i = 0; i < jComboBox1.getItemCount(); i++) {
            String[] item = jComboBox1.getItemAt(i).toString().split(" - ");
            if (item[1].equals(getKategori())) {
                jComboBox1.setSelectedIndex(i);
                break;
            }
        }
        for (int i = 0; i < jComboBox2.getItemCount(); i++) {
            String[] item = jComboBox2.getItemAt(i).toString().split(" - ");
            if (item[1].equals(getSuplier())) {
                jComboBox2.setSelectedIndex(i);
                break;
            }
        }
        jTextField5.setText(String.valueOf(getHargaJual()));
        jTextField6.setText(String.valueOf(getHargaBeli()));
        jTextField7.setText(String.valueOf(getStok()));
    }//GEN-LAST:event_formWindowOpened

