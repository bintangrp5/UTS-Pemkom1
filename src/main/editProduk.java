    private void getProductCategory() {
        try {
            Connection K = koneksi.konek();
            Statement S = K.createStatement();
            String Q = "SELECT id, nama FROM produk_kategori";
            ResultSet R = S.executeQuery(Q);
            jComboBox1.removeAllItems();
            while (R.next()) {                 
                int id = R.getInt("id");
                String name = R.getString("nama");
                jComboBox1.addItem(id+" - "+name);
            } 
        } catch (SQLException e) {
        }
    }

    private void getProductSuplier() {
        try {
            Connection K = koneksi.konek();
            Statement S = K.createStatement();
            String Q = "SELECT id, nama FROM supplier";
            ResultSet R = S.executeQuery(Q);
            jComboBox2.removeAllItems();
            while (R.next()) {                 
                int id = R.getInt("id");
                String name = R.getString("nama");
                jComboBox2.addItem(id+" - "+name);
            } 
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }
    
    private void numberOnly(KeyEvent evt) {
        char c = evt.getKeyChar();
        if(!Character.isDigit(c)){
            evt.consume();
        }
    }
    
