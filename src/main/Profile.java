    private void bt_tutupprofileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_tutupprofileActionPerformed
       // Cek apakah level pengguna sudah diatur
    if (CurrentUser.getLevel() != null) {
        // Tampilkan halaman berdasarkan level pengguna
        if (CurrentUser.getLevel().equals("admin")) {
            AdminPage adminPage = new AdminPage();
            adminPage.setVisible(true);
            this.dispose(); // Tutup halaman saat ini setelah halaman baru dibuka
        } else if (CurrentUser.getLevel().equals("kasir")) {
            KasirPage kasirPage = new KasirPage();
            kasirPage.setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Level pengguna tidak dikenal.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    } else {
        JOptionPane.showMessageDialog(this, "Level pengguna belum diatur.", "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_bt_tutupprofileActionPerformed

