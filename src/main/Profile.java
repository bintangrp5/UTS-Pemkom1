    // Metode untuk menampilkan profil pengguna
    public void tampilkanProfil(String username) {
        Connection conn = koneksi.konek();
        PreparedStatement pstmt = null;
        ResultSet rs = null;

        try {
            System.out.println("Username yang dicari: " + username);

            String sql = "SELECT * FROM user WHERE username = ?";
            pstmt = conn.prepareStatement(sql);
            pstmt.setString(1, username);
            rs = pstmt.executeQuery();

            if (rs.next()) {
                // Ambil data pengguna
                String fullname = rs.getString("fullname");
                String userUsername = rs.getString("username");
                String password = rs.getString("password");
                String level = rs.getString("level");

                // Tampilkan data pengguna di antarmuka
                lbl_fullnamevalue.setText(fullname);       // Nilai fullname
                lbl_usernamevalue.setText(userUsername);    // Nilai username
                lbl_levelvalue.setText(level);            // Nilai level atau jabatan
            } else {
                System.out.println("Pengguna tidak ditemukan");
                JOptionPane.showMessageDialog(this, "Pengguna tidak ditemukan");
            }
        } catch (SQLException e) {
            e.printStackTrace();
        } finally {
            try {
                if (rs != null) {
                    rs.close();
                }
                if (pstmt != null) {
                    pstmt.close();
                }
                if (conn != null) {
                    conn.close();
                }
            } catch (SQLException e) {
                e.printStackTrace();
            }
        }
    }
